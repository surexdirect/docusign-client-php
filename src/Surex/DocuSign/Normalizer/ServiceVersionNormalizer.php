    {
        $data = new \stdClass();
        if (null !== $object->getVersion()) {
            $data->{'version'} = $object->getVersion();
        }
        if (null !== $object->getVersionUrl()) {
            $data->{'versionUrl'} = $object->getVersionUrl();
        }

